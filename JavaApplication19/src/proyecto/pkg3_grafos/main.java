    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Scanner sc = null;
        File archivo = null;
        String vertex1;
        String vertex2;
        int i = 0;
        try {
            JFileChooser file = new JFileChooser();
            file.showOpenDialog(this);
            archivo = file.getSelectedFile();
            sc = new Scanner(archivo);
            sc.useDelimiter(",");
            while (sc.hasNext()) {
                vertex1 = sc.next();
                vertex2 = sc.next();
                sc.nextLine();
                vertex = graph.getVertices().toArray();
                if (vertex.length == 0) {
                    graph.addVertex(vertex1);
                    graph.addVertex(vertex2);
                    graph.addEdge("Edge" + i, vertex1, vertex2, EdgeType.DIRECTED);
                }
                for (int j = 0; j < vertex.length; j++) {
                    String tmp = (String) vertex[j];
                    if (tmp.equals(vertex1)) {
                        graph.addVertex(vertex2);
                        graph.addEdge("Edge" + i, tmp, vertex2, EdgeType.DIRECTED);
                    } else if (tmp.equals(vertex2)) {
                        graph.addVertex(vertex1);
                        graph.addEdge("Edge" + i, vertex1, tmp, EdgeType.DIRECTED);
                    } else {
                        graph.addVertex(vertex1);
                        graph.addVertex(vertex2);
                        graph.addEdge("Edge" + i, vertex1, vertex2, EdgeType.DIRECTED);
                    }
                }
                i++;
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(rootPane, "NO se cargaron las relaciones con exito!");
        } finally {
            sc.close();
        }
        JOptionPane.showMessageDialog(rootPane, "Se cargaron las relaciones con exito!");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Layout<String, String> layout = new CircleLayout(graph);
        layout.setSize(new Dimension(550, 550));
        BasicVisualizationServer<String, String> visualization = new BasicVisualizationServer<String, String>(layout);
        visualization.setPreferredSize(new Dimension(600, 600));
        visualization.getRenderContext().setLabelOffset(20);
        visualization.getRenderContext().setVertexLabelTransformer(new ToStringLabeller());
        JFrame frame = new JFrame("Amigos");
        frame.getContentPane().add(visualization);

        frame.pack();
        frame.setVisible(true);
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    }//GEN-LAST:event_jButton2ActionPerformed

