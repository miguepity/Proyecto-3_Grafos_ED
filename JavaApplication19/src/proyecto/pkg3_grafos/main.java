    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        edges = graph.getEdges().toArray();
        for (int i = 0; i < edges.length; i++) {
            if (graph.getEndpoints(edges[i]).getFirst().equals(cb_a.getSelectedItem().toString())
                    && graph.getEndpoints(edges[i]).getSecond().equals(cb_b.getSelectedItem().toString())) {

                JOptionPane.showMessageDialog(rootPane, "Hay coneccion de amigos entre " + cb_a.getSelectedItem().toString()
                        + " y " + cb_b.getSelectedItem().toString());
            } 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

